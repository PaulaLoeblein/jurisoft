    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        Clientes cliente = new Clientes();
        Endereco endereco = new Endereco();
        ClienteEndereco ce = new ClienteEndereco();
        ClientesDAO cliDAO = new ClientesDAO();
        EnderecoDAO endDAO  = new EnderecoDAO();
        ClienteEnderecoDAO ceDAO = new ClienteEnderecoDAO();
        
        cliente.setNomeCli(txtNomeCli.getText());
        cliente.setSexoCli(String.valueOf(cmbSexoCli.getSelectedItem()));
        cliente.setRendaCli(txtRendaFamiliarCli.getText());
        cliente.setRgCli(txtRGCli.getText());
        cliente.setEstadoCivilCli(String.valueOf(cmbEstCivilCli.getSelectedItem()));
        cliente.setNaturalidadeCli(txtNaturalidadeCli.getText());
        cliente.setNacionalidadeCli(txtNacionalidadeCli.getText());
        cliente.setFiliacaoPaiCli(txtPaiCli.getText());
        cliente.setFiliacaoMaeCli(txtMaeCli.getText());
        cliente.setCpfCli(txtCPFCli.getText());
        cliente.setTelefoneCli(txtTelefoneCli.getText());
        cliente.setTelefone2Cli(txtTelefone2Cli.getText());
        cliente.setEmailCli(txtEmailCli.getText());
        cliente.setCnhCli(txtCNHCli.getText());
        cliente.setDataNascCli(txtNascCli.getText());
        cliente.setDataCadastroCli(txtDataCadastroCli.getText());
        
        endereco.setBairroEnd(txtBairroEnd.getText());
        endereco.setRuaEnd(txtRuaEnd.getText());
        endereco.setNumeroEnd(txtNumeroEnd.getText());
        endereco.setCidadeEnd(txtCidadeEnd.getText());
        endereco.setUfEnd(String.valueOf(cmbUFEnd.getSelectedItem()));

        cliDAO.cadastrarClientes(cliente);
        endDAO.cadastrarEndereco(endereco);
        
        ce.setCodCli(cliDAO.carregarCodCli());
        ce.setCodEnd(endDAO.carregarCodEnd());
        ceDAO.vincularClienteEndereco(ce);
      
        btnInserir.setEnabled(true);
        travarComponentes();
        
    }//GEN-LAST:event_btnSalvarActionPerformed

